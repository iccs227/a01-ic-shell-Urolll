void run_external(const char* command, char* const args[]) {
    pid_t pid = fork();
    if (pid == 0) {
        execvp(command, args);
        printf("bad command\n");
        exit(1);
    }
    else {
        int status;
        waitpid(pid, &status, 0);
    }
}
